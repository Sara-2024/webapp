# SQLファイルに分割（50本ずつ）
batch_size = 50
num_batches = (len(candles) + batch_size - 1) // batch_size

for batch_num in range(num_batches):
    start_idx = batch_num * batch_size
    end_idx = min((batch_num + 1) * batch_size, len(candles))
    batch_candles = candles[start_idx:end_idx]
    
    sql_file = f"/tmp/insert_batch_{batch_num + 1}.sql"
    
    with open(sql_file, 'w') as f:
        for candle in batch_candles:
            # ヒゲなしなので high=max(open,close), low=min(open,close)
            high = max(candle['open'], candle['close'])
            low = min(candle['open'], candle['close'])
            
            f.write(f"INSERT INTO gold10_candles (timestamp, open, high, low, close, rsi) VALUES ({candle['time']}, {candle['open']}, {high}, {low}, {candle['close']}, 50);\n")
    
    print(f"Created {sql_file}")

print(f"\n合計 {len(candles)} 本のローソク足データを {num_batches} ファイルに分割しました")
